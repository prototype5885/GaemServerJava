package org.ProToType.ClassesShared;

public class InitialData {
    public int rv;
    public int i;
    public int mp;
    public int tr;
    public PlayerData[] pda;

    public int getRv() {
        return rv;
    }

    public void setRv(int rv) {
        this.rv = rv;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getMp() {
        return mp;
    }

    public void setMp(int mp) {
        this.mp = mp;
    }

    public int getTr() {
        return tr;
    }

    public void setTr(int tr) {
        this.tr = tr;
    }

    public PlayerData[] getPda() {
        return pda;
    }

    public void setPda(PlayerData[] pda) {
        this.pda = pda;
    }
}
