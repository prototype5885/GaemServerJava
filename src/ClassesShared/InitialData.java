package org.GaemServer.ClassesShared;

public class InitialData {
    public int lr;
    public int i;
    public int mp;
    public int tr;
    public PlayerData[] pda;

    public int getLr() {
        return lr;
    }

    public void setLr(int lr) {
        this.lr = lr;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getMp() {
        return mp;
    }

    public void setMp(int mp) {
        this.mp = mp;
    }

    public int getTr() {
        return tr;
    }

    public void setTr(int tr) {
        this.tr = tr;
    }

    public PlayerData[] getPda() {
        return pda;
    }

    public void setPda(PlayerData[] pda) {
        this.pda = pda;
    }
}
